# DAY 1 TASK NO.11 === Write a Program to accept a number from 1 to 7 and display the name of the day like 1 for sunday,2 for monday and so on. 


i=int(input('ENTER ANY NUMBER AND GET NAME OF THE DAY :'))
if i==1:
        print('--SUNDAY--(FuN DaY)')
elif i==2:
        print('--MONDAY--')
elif i==3:
        print('--TUESDAY--')
elif i==4:
        print('--WEDNESDAY--')
elif i==5:
        print('--THRUSDAY--')
elif i==6:
        print('--FRIDAY--')
elif i==7:
        print('--SATURDAY--')