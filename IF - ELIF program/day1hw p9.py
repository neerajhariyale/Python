#  DAY 1 TASK NO.7 === Write a program to check whether the last digit of a number(entered by user) is divisible by 3 or NOT.


i = int(input('ENTER A NUMBER :'))
print(i%10)

if i%10%3==0:
    print('YES ,IT IS DIVISIBLE BY 3.')
elif i==i:
    print('NO,IT IS NOT DIVISIBLE BY 3.')
 
     